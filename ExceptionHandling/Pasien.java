public class Pasien {
        private String nama;
        private String nomorAntrean;
    
        public Pasien(String nama, String nomorAntrean) {
            this.nama = nama;
            this.nomorAntrean = nomorAntrean;
        }
    
        public String getNama() {
            return nama;
        }
    
        public String getAntrean() {
            return nomorAntrean;
        }
    }
    
